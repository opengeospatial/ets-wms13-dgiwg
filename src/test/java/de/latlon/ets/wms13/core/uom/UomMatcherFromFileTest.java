package de.latlon.ets.wms13.core.uom;

import static org.hamcrest.CoreMatchers.is;
import static org.junit.Assert.assertThat;

import org.junit.Test;

/**
 * @author <a href="mailto:goltz@lat-lon.de">Lyn Goltz</a>
 */
public class UomMatcherFromFileTest {

	private final UomMatcherFromFile uomMatcher = new UomMatcherFromFile();

	@Test
	public void testIsExpectedUoM_expected() {
		boolean isExpectedUom = uomMatcher.isExpectedUoM("K");
		assertThat(isExpectedUom, is(true));
	}

	@Test
	public void testIsExpectedUoM_unexpected() {
		boolean isExpectedUom = uomMatcher.isExpectedUoM("Anzahl Kisten");
		assertThat(isExpectedUom, is(false));
	}

	@Test
	public void testIsExpectedUoM_null() {
		boolean isExpectedUom = uomMatcher.isExpectedUoM(null);
		assertThat(isExpectedUom, is(false));
	}

}